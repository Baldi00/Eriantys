    SceneSprite() {
    }

    /**
     * Associates an id to the constant
     *
     * @param id the id
     */
    SceneSprite(int id) {
        this.id = id;
    }

    SceneSprite(Student student) {
        this.student = student;
    }

    SceneSprite(int characterId, boolean isCharacter) {
        this.id = characterId;
        this.isCharacter = isCharacter;
    }

    SceneSprite(int characterId, Student student) {
        this.id = characterId;
        this.isCharacter = true;
        this.student = student;
    }

    /**
     * @return the id associated to the SceneSprite.
     * @throws IllegalCallerException when call from the wrong SceneSprite.
     */
    public int getId() {
        if (id == -1 && !isCharacter)
            throw new IllegalCallerException("Cannot get id from " + name());
        return id;
    }

    /**
     * @return the student associated to the SceneSprite.
     * @throws IllegalCallerException when call from the wrong SceneSprite.
     */
    public Student getStudentColor() {
        if (student == null)
            throw new IllegalCallerException("Cannot get Student from " + name());
        return student;
    }

    /**
     * @return 0, 1, 2 depending on which of the three characters is selected.
     * @throws IllegalCallerException when call from the wrong SceneSprite.
     */
    public int getCharacterId() {
        if (!isCharacter)
            throw new IllegalCallerException("Cannot get character id from " + name());
        return id;
    }

